    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        Enable();
        edit = true;
        row = timekeeperTBL.getSelectedRow();
        if (row < 0 || row > model.getRowCount()) {
            JOptionPane.showMessageDialog(this, "Chon dong de sua!");
        } else {
            idTF.setText(model.getValueAt(row, 0).toString());
            dateTimeTF.setText(model.getValueAt(row, 1).toString());
            if (model.getValueAt(row, 2).toString().compareToIgnoreCase("0")==0) offBtn.setSelected(true);
            else onBtn.setSelected(true);
            empIDList.setSelectedItem(new Object[]{model.getValueAt(row, 3)});
            edit = false;
        }
        
    }//GEN-LAST:event_editBtnActionPerformed

    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        row = timekeeperTBL.getSelectedRow();
        if (row < 0 || row > model.getRowCount()) {
            JOptionPane.showMessageDialog(this, "Chon dong de xoa!");
        } else {
            TimekeeperDAO dao = new TimekeeperDAO();
            dao.deleteTimekeeper(model.getValueAt(row, 0).toString());
            model.removeRow(row);
        }
    }//GEN-LAST:event_delBtnActionPerformed

    private void showBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showBtnActionPerformed
        TimekeeperDAO dao = new TimekeeperDAO();
        list = dao.selectAllTimekeepers();
        for (Timekeeper item : list){
            model.addRow(item.toObject());
        }
        buttonTrue();
    }//GEN-LAST:event_showBtnActionPerformed


