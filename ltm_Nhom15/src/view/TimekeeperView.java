    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        TimekeeperDAO dao = new TimekeeperDAO();
        String in_out="";
        if (offBtn.isSelected()) in_out = "0";
        else in_out= "1";
        Date date = null;
      try {
          date = new SimpleDateFormat("dd/MM/yyyy").parse(dateTimeTF.getText());
      } catch (ParseException ex) {
          Logger.getLogger(TimekeeperView.class.getName()).log(Level.SEVERE, null, ex);
      }
        Timekeeper newTKP = new Timekeeper(idTF.getText(),date,BigInteger.valueOf(Long.parseLong(empIDList.getSelectedItem().toString())),in_out);
        if(add){
        dao.insertTimekeeper(newTKP);
        model.addRow(newTKP.toObject());
        add = false;
        Disnable();
        buttonTrue();}
        else if (edit){
            model.setValueAt(newTKP.getTimekeeper_Id().toString(), row, 0);
            model.setValueAt(new SimpleDateFormat("dd/mm/yyyy").format(newTKP.getDate_Time()), row, 1);
            model.setValueAt(newTKP.getIn_Out().toString(), row, 2);
            model.setValueAt(newTKP.getEmpId().toString(), row, 3);
            dao.editTimekeeper(newTKP, newTKP.getTimekeeper_Id());
        }
    }//GEN-LAST:event_updateBtnActionPerformed

